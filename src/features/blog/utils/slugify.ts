/**
 * Normaliza un string para usarlo como slug de URL.
 * Ejemplo: "Inteligencia Artificial" → "inteligencia-artificial"
 */
export function slugify(text: string): string {
  return text
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .toLowerCase()
    .trim()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "");
}
